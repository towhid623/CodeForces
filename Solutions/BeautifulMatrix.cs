using System;
using System.Collections.Generic;
using System.Text;

namespace Solutions
{
    public class BeautifulMatrix
    {
        public void Solve()
        {
            var arr = new int[][] { };
            for (int i = 0; i < 5; i++)
            {
                for (int j = 0; j < 5; j++)
                {
                    arr[i][j] = int.Parse(Console.ReadLine())
                }
            }
        }
    }
}
